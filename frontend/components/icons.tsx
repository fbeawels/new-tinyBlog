// Icon component that supports both custom and Lucide icons
const Icon = ({ 
  name, 
  size = 24, 
  className = "", 
  ...props 
}: IconProps) => {
  // First check if it's a custom icon
  const CustomIcon = Icons[name as keyof typeof Icons]
  if (CustomIcon) {
    return <CustomIcon className={className} {...props} />
  }
  
  // Otherwise, try to use Lucide icons
  const LucideIcon = LucideIcons[name as keyof typeof LucideIcons] as React.ElementType
  
  if (!LucideIcon) {
    console.warn(`Icon "${name}" not found`)
    return null
  }
  
  return <LucideIcon size={size} className={className} {...props} />
}

